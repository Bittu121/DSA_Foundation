int main()
{
  int n;
  cin >> n;
  int i = 1;
  while (i <= n)
  {
    int j = 1;
    char strch = 'A' + i - 1;
    while (j <= i)
    {
      char ch = strch + j - 1;
      cout << ch;
      j++;
    }
    cout << endl;
    i++;
  }
  return 0;
}